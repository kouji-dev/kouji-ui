#!/usr/bin/env node
/**
 * Migrate examples + playgrounds into per-component `_examples/` subfolders
 * and convert the master barrel into a lazy-loader map.
 *
 * For each `export { X } from './<folder>/<file>'` line in a master barrel:
 *   1. Move `<pkg>/src/<folder>/<file>.ts` → `<pkg>/src/<folder>/_examples/<file>.ts`
 *      (idempotent — already-moved files are detected via existence checks).
 *   2. Append `export { X } from './<file>'` to `<pkg>/src/<folder>/_examples/index.ts`
 *      (deduplicated).
 *   3. Replace the master barrel with a lazy-loader manifest:
 *         export const EXAMPLE_LOADERS: Record<string, () => Promise<Record<string, unknown>>>;
 *         export const EXAMPLE_OWNER:   Record<string, string>;
 *
 * Run from the repo root:
 *   node scripts/migrate-examples.mjs
 */
import { readFile, writeFile, mkdir, rename, access } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';

const ROOT = resolve(process.cwd());
const PKGS = [
  { name: '@kouji-ui/components', barrel: 'packages/components/src/example-components.ts', src: 'packages/components/src' },
  { name: '@kouji-ui/core',       barrel: 'packages/core/src/example-components.ts',       src: 'packages/core/src' },
];

const EXPORT_RE = /^export\s+\{\s*([A-Za-z_$][\w$]*)\s*\}\s+from\s+['"]\.\/([^/]+)\/([^'"]+)['"]\s*;?/;

async function fileExists(p) { try { await access(p); return true; } catch { return false; } }

for (const pkg of PKGS) {
  const barrelPath = join(ROOT, pkg.barrel);
  const text = await readFile(barrelPath, 'utf-8');
  const lines = text.split(/\r?\n/);

  /** folder → { entries: Map<exportName, fileBaseWithoutExt> } */
  const folders = new Map();
  /** exportName → folder */
  const owner = {};

  for (const line of lines) {
    const m = line.match(EXPORT_RE);
    if (!m) continue;
    const [, exportName, folder, fileWithMaybeExt] = m;
    // Path on disk currently lives at <pkg.src>/<folder>/<fileWithMaybeExt>(.ts)
    const fileBase = fileWithMaybeExt.replace(/\.ts$/, '');
    if (!folders.has(folder)) folders.set(folder, new Map());
    folders.get(folder).set(exportName, fileBase);
    owner[exportName] = folder;
  }

  // Move files + generate per-folder barrels.
  for (const [folder, entries] of folders) {
    const folderDir = join(ROOT, pkg.src, folder);
    const examplesDir = join(folderDir, '_examples');
    await mkdir(examplesDir, { recursive: true });

    for (const [, fileBase] of entries) {
      const srcFile = join(folderDir, `${fileBase}.ts`);
      const dstFile = join(examplesDir, `${fileBase}.ts`);
      if (await fileExists(srcFile)) {
        // Already-moved files won't be at srcFile; skip if so.
        if (!(await fileExists(dstFile))) {
          await rename(srcFile, dstFile);
        }
      }
    }

    // Write per-folder barrel.
    const barrelLines = [...entries.entries()]
      .map(([name, file]) => `export { ${name} } from './${file}';`)
      .sort();
    const indexPath = join(examplesDir, 'index.ts');
    await writeFile(
      indexPath,
      [
        '// AUTO-GENERATED by scripts/migrate-examples.mjs',
        '// One barrel per component folder — used by the docs app as a lazy chunk.',
        ...barrelLines,
        '',
      ].join('\n'),
      'utf-8',
    );
  }

  // Rewrite master barrel.
  const folderEntries = [...folders.keys()].sort();
  const ownerLines = Object.entries(owner)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, fold]) => `  ${name}: '${fold}',`);

  const master = `// AUTO-GENERATED by scripts/migrate-examples.mjs
// Map of folder → lazy loader for that folder's example components,
// plus a static exportName → folder owner index.
//
// Consumers (apps/docs ExampleRegistryService) look up an export name in
// EXAMPLE_OWNER to find its folder chunk, then call EXAMPLE_LOADERS[folder]()
// to import the (code-split) module containing it.
//
// Each entry below produces its own chunk in the build output — opening
// the docs page for component X only pulls component X's examples, not the
// entire library.

import type { Type } from '@angular/core';

export const EXAMPLE_LOADERS: Record<string, () => Promise<Record<string, Type<unknown>>>> = {
${folderEntries.map((f) => `  '${f}': () => import('./${f}/_examples') as Promise<Record<string, Type<unknown>>>,`).join('\n')}
};

export const EXAMPLE_OWNER: Record<string, string> = {
${ownerLines.join('\n')}
};
`;

  await writeFile(barrelPath, master, 'utf-8');
  // eslint-disable-next-line no-console
  console.log(`✓ ${pkg.name}: ${folders.size} folders, ${Object.keys(owner).length} examples`);
}
